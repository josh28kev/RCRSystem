    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        boolean b = false;
        try {
            Bulto bulto = controller.obtener_bulto(codigobulto);
            if (jComboBox1.getSelectedItem() == "1") {
                pesos.add(new BigDecimal(jTextField3.getText()));
            } else if (jComboBox1.getSelectedItem() == "2") {
                pesos.add(new BigDecimal(jTextField3.getText()));
                pesos.add(new BigDecimal(jTextField4.getText()));
            } else if (jComboBox1.getSelectedItem() == "3") {
                pesos.add(new BigDecimal(jTextField3.getText()));
                pesos.add(new BigDecimal(jTextField4.getText()));
                pesos.add(new BigDecimal(jTextField5.getText()));
            } else if (jComboBox1.getSelectedItem() == "4") {
                pesos.add(new BigDecimal(jTextField3.getText()));
                pesos.add(new BigDecimal(jTextField4.getText()));
                pesos.add(new BigDecimal(jTextField5.getText()));
                pesos.add(new BigDecimal(jTextField6.getText()));
            } else if (jComboBox1.getSelectedItem() == "5") {
                pesos.add(new BigDecimal(jTextField3.getText()));
                pesos.add(new BigDecimal(jTextField4.getText()));
                pesos.add(new BigDecimal(jTextField5.getText()));
                pesos.add(new BigDecimal(jTextField6.getText()));
                pesos.add(new BigDecimal(jTextField7.getText()));
            }
            b = controller.agregar_bulto(bulto, pesos);
            model.cargar_bultos(bulto.obtener_material().obtener_codigo().substring(1));
        } catch (Exception e) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null, "¡El Peso no es válido!");
        }
        pesos.clear();
        if(b) this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed
    
