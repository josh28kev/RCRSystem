    @Override
    public void update(java.util.Observable updatedModel, Object parametros) {
        if (parametros != Consulta_Proveedor_Modelo.ae_bulto_modelo) {
            return;
        }
        
        Bulto actual = model.getA_modelo_b_c().obtener_bultos_C_Modelo().obtener_fila_a(numero_fila);
        if (actual != null) {
            this.jTextFieldCodigo.setText(actual.obtener_codigo());
            this.jTextFieldMaterial.setText(actual.obtener_material().obtener_t_material().obtener_nombre());
            this.jTextFieldPeso.setText(String.valueOf(actual.obtener_peso()));
        }
    }
