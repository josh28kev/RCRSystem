    @Override
    public void update(java.util.Observable updatedModel, Object parametros) {
        if (parametros != Proveedor_Modelo.ae_usuario_actual) {
            return;
        }
        Proveedor usuarioCurrent = modelo.obtener_proveedor_actual();
        if (usuarioCurrent == null) {
            this.jTextField_id.setText("");
            this.jTextField_nombre.setText("");
            this.jTextField_lugar.setText("");
            this.jTextField_Contacto.setText("");
            this.jTextField_telCelular.setText("");
            this.jTextField_telFijo.setText("");
        } else {
            this.jTextField_id.setText(usuarioCurrent.obtener_codigo());
            if (modelo.obtener_errores().get("id") != null) {
                this.jTextField_id.setBorder(BORDER_ERROR);
                this.jTextField_id.setToolTipText(modelo.obtener_errores().get("id"));
            } else {
                this.jTextField_id.setBorder(BORDER_NOBORDER);
                this.jTextField_id.setToolTipText(null);
            }
            this.jTextField_nombre.setText(usuarioCurrent.obtener_nombre());
            if (modelo.obtener_errores().get("nombre") != null) {
                this.jTextField_nombre.setBorder(BORDER_ERROR);
                this.jTextField_nombre.setToolTipText(modelo.obtener_errores().get("nombre"));
            } else {
                this.jTextField_nombre.setBorder(BORDER_NOBORDER);
                this.jTextField_nombre.setToolTipText(null);
            }
            this.jTextField_lugar.setText(usuarioCurrent.obtener_lugar());
            if (modelo.obtener_errores().get("lugar") != null) {
                this.jTextField_lugar.setBorder(BORDER_ERROR);
                this.jTextField_lugar.setToolTipText(modelo.obtener_errores().get("lugar"));
            } else {
                this.jTextField_lugar.setBorder(BORDER_NOBORDER);
                this.jTextField_lugar.setToolTipText(null);
            }
            this.jTextField_Contacto.setText(usuarioCurrent.obtener_personaContacto());
            if (modelo.obtener_errores().get("contacto") != null) {
                this.jTextField_Contacto.setBorder(BORDER_ERROR);
                this.jTextField_Contacto.setToolTipText(modelo.obtener_errores().get("contacto"));
            } else {
                this.jTextField_Contacto.setBorder(BORDER_NOBORDER);
                this.jTextField_Contacto.setToolTipText(null);
            }
            if(usuarioCurrent.obtener_telCelular()==0 && modelo.obtener_modo()!=rcrsystem.Aplicacion.ae_modo_editar){
             this.jTextField_telCelular.setText("");
            }else{
            this.jTextField_telCelular.setText(Integer.toString(usuarioCurrent.obtener_telCelular()));
            }
            if (modelo.obtener_errores().get("celular") != null) {
                this.jTextField_telCelular.setBorder(BORDER_ERROR);
                this.jTextField_telCelular.setToolTipText(modelo.obtener_errores().get("celular"));
            } else {
                this.jTextField_telCelular.setBorder(BORDER_NOBORDER);
                this.jTextField_telCelular.setToolTipText(null);
            }
            if(usuarioCurrent.obtener_telFijo()==0 && modelo.obtener_modo()!=rcrsystem.Aplicacion.ae_modo_editar){
             this.jTextField_telFijo.setText("");
            }else{
            this.jTextField_telFijo.setText(Integer.toString(usuarioCurrent.obtener_telFijo()));
            }
            if (modelo.obtener_errores().get("fijo") != null) {
                this.jTextField_telFijo.setBorder(BORDER_ERROR);
                this.jTextField_telFijo.setToolTipText(modelo.obtener_errores().get("fijo"));
            } else {
                this.jTextField_telFijo.setBorder(BORDER_NOBORDER);
                this.jTextField_telFijo.setToolTipText(null);
            }

          
        }
        this.validate();
        if (modelo.obtener_mensaje() != null) {
            if (!modelo.obtener_mensaje().equals("")) {
                JOptionPane.showMessageDialog(this, modelo.obtener_mensaje(), "", JOptionPane.INFORMATION_MESSAGE);
                modelo.colocar_mensaje("");
            }
        }
    }
