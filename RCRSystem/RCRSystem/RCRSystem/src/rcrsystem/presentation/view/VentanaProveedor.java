    private void jTextField_telCelularKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_telCelularKeyTyped
        if (this.jTextField_telCelular.getText().length() < 9) {
        if (!Character.isDigit(evt.getKeyChar()) && !Character.isISOControl(evt.getKeyChar())) {
            Toolkit.getDefaultToolkit().beep();
            evt.consume();
        }
        }
        else{
         Toolkit.getDefaultToolkit().beep();
            evt.consume();
        } 
        
    }//GEN-LAST:event_jTextField_telCelularKeyTyped

